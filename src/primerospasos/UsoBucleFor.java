public class UsoBucleFor {

	public static void main(String[] args) {
		
		System.out.println("Numero del 1 al 10: ");
		for (int i = 1; i <= 10; i++ ) {
			System.out.println(i);
		}
		
		System.out.println("\nNumero del 10 al 1: ");
		for (int i = 10; i > 0; i--) {
			System.out.println(i);
		}

		
	}

}
